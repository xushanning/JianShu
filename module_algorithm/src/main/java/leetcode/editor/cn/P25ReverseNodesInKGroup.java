    class Solution {
        public ListNode reverseKGroup(ListNode head, int k) {
            // ç»™ä½ è¿™ä¸ªé“¾è¡¨ï¼š0->1->2->3->4->5
            // å½“ k = 2 æ—¶ï¼Œåº”å½“è¿”å›ž: 2->1->4->3->5
            if (head == null) {
                return null;
            }
            ListNode dummy = new ListNode(0);
            dummy.next = head;
            ListNode pre = dummy;
            ListNode end = dummy;
            while (true) {
                for (int i = 0; i < k; i++) {
                    if (end != null) {
                        end = end.next;
                    }
                }
                if (end == null) {
                    break;
                }
                ListNode start = pre.next;
                ListNode next = end.next;
                end.next = null;
                pre.next = reverse(start);
                start.next = next;
                pre = start;
                end = start;
            }

            return dummy.next;
        }

        //0->1->2
        private ListNode reverse(ListNode node) {
            ListNode pre = null;
            ListNode cur = node;
            while (cur != null) {
                ListNode next = cur.next;
                cur.next = pre;
                pre = cur;
                cur = next;
            }
            return pre;
        }
    }
