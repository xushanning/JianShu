    class LRUCache {
        private int capacity;
        private Node head;
        private Node tail;
        private Map<Integer, Node> data;
        private int size = 0;

        public LRUCache(int capacity) {
            this.capacity = capacity;
            data = new HashMap<>();
            head = new Node();
            tail = new Node();
            head.next = tail;
            tail.pre = head;
        }

        public int get(int key) {
            Node cur = data.get(key);
            if (cur == null) {
                return -1;
            } else {
                moveToTop(cur);
                return cur.value;
            }
        }

        public void put(int key, int value) {
            Node cur = data.get(key);
            if (cur == null) {
                Node newNode = new Node();
                newNode.key = key;
                newNode.value = value;
                data.put(key, newNode);
                size++;
                addNode(newNode);
                if (size > capacity) {
                    Node tail = popTail();
                    data.remove(tail.key);
                    size--;
                }
            } else {
                cur.value = value;
                moveToTop(cur);
            }
        }

        private void moveToTop(Node node) {
            deleteNode(node);
            addNode(node);
        }

        private Node popTail() {
            Node pre = tail.pre;
            deleteNode(pre);
            return pre;
        }

        private void addNode(Node node) {
            Node next = head.next;
            head.next = node;
            node.next = next;
            next.pre = node;
            node.pre = head;
        }

        private void deleteNode(Node node) {
            Node pre = node.pre;
            Node next = node.next;
            pre.next = next;
            next.pre = pre;
        }


        class Node {
            Node pre;
            Node next;
            int key;
            int value;
        }
    }

