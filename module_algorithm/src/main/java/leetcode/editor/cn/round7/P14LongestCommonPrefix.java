    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public String longestCommonPrefix(String[] strs) {
// 输入: ["flower","flow","flight"]
//输出: "fl"
            int len = strs.length;
            if (len == 0) {
                return "";
            }
            String res = strs[0];
            for (String s : strs) {
                while (s.indexOf(res) != 0) {
                    res = res.substring(0, res.length() - 1);
                }
            }
            return res;
        }
    }
