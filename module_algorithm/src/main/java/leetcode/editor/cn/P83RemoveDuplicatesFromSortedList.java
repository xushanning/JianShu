    class Solution {
        public ListNode deleteDuplicates(ListNode head) {
// è¾“å…¥: 1->1->2->3->3
//è¾“å‡º: 1->2->3
            if (head == null) {
                return null;
            }
            ListNode cur = head;
            while (cur.next != null) {
                if (cur.val == cur.next.val) {
                    cur.next = cur.next.next;
                } else {
                    cur = cur.next;
                }
            }
            return head;
        }
    }
