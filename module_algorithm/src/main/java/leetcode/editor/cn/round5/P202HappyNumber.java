    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public boolean isHappy(int n) {
            Set<Integer> set = new HashSet<>();
            set.add(n);
            while (n != 1) {
                n = check(n);
                if (!set.add(n)) {
                    return false;
                }
            }
            return true;
        }

        //1 9  =
        private int check(int n) {
            int res = 0;
            while (n != 0) {
                int a = n % 10;
                res = res + a * a;
                n /= 10;
            }
            return res;
        }
    }
