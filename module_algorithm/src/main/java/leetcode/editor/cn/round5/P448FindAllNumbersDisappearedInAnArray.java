    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public List<Integer> findDisappearedNumbers(int[] nums) {
            int len = nums.length;
            List<Integer> res = new ArrayList<>();
            if (len == 0) {
                return res;
            }
            Map<Integer, Integer> map = new HashMap<>();
            for (int n : nums) {
                map.put(n, 0);
            }

            for (int i = 1; i <=len; i++) {
                if (map.get(i) == null) {
                    res.add(i);
                }
            }
            return res;
        }
    }
