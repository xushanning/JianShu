    class Solution {
        public void nextPermutation(int[] nums) {
            //第一步：1 2 3 4 6 5
            //第二步：1 2 3 5 6 4
            //第三步：1 2 3 5 4 6

            int len = nums.length;
            if (len == 0) {
                return;
            }
            int i = len - 2;
            //找到位置
            while (i >= 0 && nums[i + 1] <= nums[i]) {
                i--;
            }
            //如果能找到
            if (i >= 0) {
                int j = len - 1;
                //找到右边
                while (j >= 0 && nums[j] <= nums[i]) {
                    j--;
                }
                swap(nums, i, j);
            }
            reverse(nums, i + 1);
        }

        private void reverse(int[] nums, int start) {
            int i = start, j = nums.length - 1;
            while (i < j) {
                swap(nums, i, j);
                i++;
                j--;
            }
        }

        private void swap(int[] nums, int i, int j) {
            int temp = nums[i];
            nums[i] = nums[j];
            nums[j] = temp;
        }
    }
